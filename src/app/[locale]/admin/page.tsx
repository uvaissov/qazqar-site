async function getStats() {
  const [
    totalCars,
    availableCars,
    pendingBookings,
    totalBookings,
    blogPosts,
    totalReviews,
  ] = await Promise.all([
    prisma.car.count(),
    prisma.car.count({ where: { status: "AVAILABLE" } }),
    prisma.booking.count({ where: { status: "PENDING" } }),
    prisma.booking.count(),
    prisma.blogPost.count(),
    prisma.review.count(),
  ]);

  return {
    totalCars,
    availableCars,
    pendingBookings,
    totalBookings,
    blogPosts,
    totalReviews,
  };
}

